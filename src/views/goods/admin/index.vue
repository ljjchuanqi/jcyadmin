<template>
  <yxb-container :is-back-to-top="true">
    <page-header
      slot="header"
      :loading="loading"
      @submit="handleSubmit"
      ref="header"/>

    <page-main
      :table-data="table"
      :tabs-config="tabs"
      :loading="loading"
      @sort="handleSort"
      @tabs="handleTabs"
      @refresh="handleRefresh"/>

    <page-footer
      slot="footer"
      :current="page.current"
      :loading="loading"
      :size="page.size"
      :total="page.total"
      @change="handlePaginationChange"/>
  </yxb-container>
</template>

<script>
import { mapActions } from 'vuex'
import { getGoodsAdminList } from '@/api/goods/goods'

export default {
  name: 'goods-admin-list',
  components: {
    'PageHeader': () => import('./components/PageHeader'),
    'PageMain': () => import('./components/PageMain'),
    'PageFooter': () => import('@/layout/header-aside/components/footer')
  },
  data() {
    return {
      table: [],
      loading: true,
      page: {
        current: 1,
        size: 0,
        total: 0
      },
      tabs: {
        status: 1,
        is_delete: 0
      },
      order: {
        order_type: undefined,
        order_field: undefined
      }
    }
  },
  mounted() {
    this.$store.dispatch('ldyxb/db/databasePage', { user: true })
      .then(res => {
        this.page.size = res.get('size').value() || 25
      })
      .then(() => {
        this.handleSubmit()
      })
  },
  methods: {
    ...mapActions('ldyxb/update', [
      'updateData'
    ]),
    // 刷新列表页面
    handleRefresh(isTurning = false) {
      if (isTurning) {
        !(this.page.current - 1) || this.page.current--
      }

      this.$nextTick(() => {
        this.$refs.header.handleFormSubmit()
      })
    },
    // 分页变化改动
    handlePaginationChange(val) {
      this.page = val
      this.$nextTick(() => {
        this.$refs.header.handleFormSubmit()
      })
    },
    // 排序刷新
    handleSort(val) {
      this.order = val
      this.$nextTick(() => {
        this.$refs.header.handleFormSubmit()
      })
    },
    // 标签页切换
    handleTabs(val) {
      this.tabs = val
      this.order = {}
      this.$nextTick(() => {
        this.$refs.header.handleFormSubmit(true)
      })
    },
    // 提交查询请求
    handleSubmit(form, isRestore = false) {
      if (isRestore) {
        this.page.current = 1
      }

      this.loading = true
      getGoodsAdminList({
        ...form,
        ...this.tabs,
        ...this.order,
        page_no: this.page.current,
        page_size: this.page.size,
        is_goods_spec: this.tabs.is_delete ? 0 : 1
      })
        .then(res => {
          this.updateData({ type: 'clear', name: 'goods-admin-list' })
          this.page.total = res.data.total_result
          this.table = res.data.total_result > 0 ? res.data.items : []
        })
        .finally(() => {
          this.loading = false
        })
    }
  }
}
</script>
