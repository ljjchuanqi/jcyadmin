import request from '@/utils/request'

/**
 * 添加一条配送轨迹
 * @param {Object} data
 * @returns {AxiosPromise}
 */
export function addDeliveryDistItem(data) {
  return request({
    url: '/v3/delivery_dist',
    method: 'post',
    params: {
      method: 'add.delivery.dist.item'
    },
    data
  })
}

